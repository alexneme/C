/*

  Course Pack 1 - Variables
  Exercise 8. Write a program that reads two integers, calculates and displays the first number multiplied by 5 and the second number added to 10.

*/

#include <stdio.h>

int main()
{
  int num1, num2;
  printf("Enter first integer number to multiply by 5: ");
  scanf("%d", &num1);
  printf("Enter second integer number to add 10: ");
  scanf("%d", &num2);
  printf("First number times 5 = %d\nSecond number plus 10 = %d", num1 = num1 * 5, num2 = num2 + 10);
  printf("\n");
  return 0;
}